#Ejemplo:

# Variable que almacena un número entero (int)
edad = 25 

# Variable que almacena un número decimal (float)
altura = 1.69

# Variable que almacena un texto (string)
nombre = "Mcsalo"

# Imprime las variables para ver sus valores
print(edad)                         # Salida: 25
print("El nombre es:", nombre)      # Salida: El nombre es: Mcsalo
print(altura)                       # Salida: 1.75

#Interactuar entre ellas
suma = edad + altura
print(f"soy la suma de edad y altura {edad} + {altura} = {suma}") #soy la suma de edad y altura 25 + 1.69 = 26.69

#Cambiar el valor de una variable
nombre = edad
print("Soy el nuevo nombre: ", nombre) # Soy el nuevo nombre: 25